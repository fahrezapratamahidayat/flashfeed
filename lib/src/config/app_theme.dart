      style: ElevatedButton.styleFrom(
        backgroundColor: AppColors.lightPrimary,
        foregroundColor: AppColors.lightPrimaryForeground,
        elevation: 0,
        shape: RoundedRectangleBorder(
          // CSS: --radius-sm: calc(var(--radius) - 4px);
